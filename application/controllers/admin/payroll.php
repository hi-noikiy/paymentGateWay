public function getTimeInMinutes($time_in, $time_out){											
	$start_time = new DateTime("$time_in");
	$end_time = new DateTime("$time_out");
	$diff = $end_time->diff($start_time);
	$min = ($diff->h * 60);
	$min += $diff->i;
	return $min;
}
